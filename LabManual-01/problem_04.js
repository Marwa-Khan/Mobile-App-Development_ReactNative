//Write a JavaScript that calculates the squares and cubes of the numbers from 0 to 10.


function square(){
    for (let num=0;num<=10;num++) {
        console.log("The squares of number "+num+" is:"+num*num);
    
        console.log("\n");
       
    
    }

}

function cube() {
    for (let num=0;num<=10;num++) {
        console.log("The cube  of number "+num+" is:"+num*num*num);
    
        console.log("\n");
       
    
    }
    
}

square();
cube();

